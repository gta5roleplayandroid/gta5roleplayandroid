const CACHE_NAME = "thinkmay-v1";
const URLS_TO_CACHE = [
  "/gta5roleplayandroid/",
  "/gta5roleplayandroid/index.html",
  "/gta5roleplayandroid/manifest.json",
  "/gta5roleplayandroid/icon-192.png",
  "/gta5roleplayandroid/icon-512.png"
];

self.addEventListener("install", event => {
  event.waitUntil(
    caches.open(CACHE_NAME).then(cache => {
      return cache.addAll(URLS_TO_CACHE);
    })
  );
  self.skipWaiting();
});

self.addEventListener("activate", event => {
  event.waitUntil(
    caches.keys().then(keys =>
      Promise.all(
        keys.map(key => {
          if (key !== CACHE_NAME) {
            return caches.delete(key);
          }
        })
      )
    )
  );
  self.clients.claim();
});

self.addEventListener("fetch", event => {
  event.respondWith(
    caches.match(event.request).then(response => {
      return response || fetch(event.request);
    })
  );
});
